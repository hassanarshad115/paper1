
public class Student {
    private String fname;
    private String lname;
 public   Student( )
 {
 fname="";
 lname="";
 
 }
 public void setfname(String f)
 {
 fname=f;
 }
 public void setlname(String l)
 {
 lname=l;
 }

  public String getfname()
  {
  return fname;
  }
 public String getlname ()
 {
 return lname;
 }

 
}

